package com.syntax.reviewclass04;

public class NestedLoopDemo3 {
	
	public static void main(String[] args) {
		
		int c=1;
		while(c<=2) {
			System.out.println(c);
			c++;
		}
		c=1;
		while(c<=2) {
			System.out.println(c);
			c++;
		}
		c=1;
		while(c<=2) {
			System.out.println(c);
			c++;
		}
	}

}
